  def solve(input: String): Int =
    val boxes = input.linesIterator
      .map:
        case s"$x,$y,$z" => (x.toLong, y.toLong, z.toLong)
      .toVector

    (for
      case Vector(fst, tail*) <- boxes.tails
      snd <- tail
    yield (fst, snd)).toVector
      .sortBy:
        case ((x1, y1, z1), (x2, y2, z2)) =>
          (x2 - x1) * (x2 - x1) + (y2 - y1) * (y2 - y1) + (z2 - z1) * (z2 - z1)
      .take(1000)
      .foldLeft(boxes.zipWithIndex.toMap):
        case (acc, (l, r)) =>
          val (cl, cr) = (acc(l), acc(r))
          acc.view.mapValues(i => if i == cr then cl else i).toMap
      .values
      .groupMapReduce(identity)(_ => 1)(_ + _)
      .values
      .toVector
      .sorted
      .takeRight(3)
      .product

  def solve2(input: String): Long =
    val boxes = input.linesIterator
      .map:
        case s"$x,$y,$z" => (x.toLong, y.toLong, z.toLong)
      .toVector

    @tailrec def loop(
      clusters: Map[(Long, Long, Long), Int],
      pairs: Vector[((Long, Long, Long), (Long, Long, Long))]
    ): Long =
      val (l, r)       = pairs.head
      val (cl, cr)     = (clusters(l), clusters(r))
      val nextClusters = clusters.view.mapValues(i => if i == cr then cl else i).toMap
      if nextClusters.values.toSet.size == 1 then l(0) * r(0) else loop(nextClusters, pairs.tail)

    loop(
      boxes.zipWithIndex.toMap,
      (for
        case Vector(fst, tail*) <- boxes.tails
        snd <- tail
      yield (fst, snd)).toVector
        .sortBy:
          case ((x1, y1, z1), (x2, y2, z2)) =>
            (x2 - x1) * (x2 - x1) + (y2 - y1) * (y2 - y1) + (z2 - z1) * (z2 - z1)
    )

